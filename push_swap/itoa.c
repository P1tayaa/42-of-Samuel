int ft_itoa(char *input)
{
    int i;
    int number;
    
    i = 0;
    number = input[i] - '0';
    i++;
    while (input[i] != '\0')
    {
        number = number * 10 + input[i] - '0';
        i++;
    }
    return (number);
}